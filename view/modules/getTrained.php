        <h4 class="display-5 text-black mb-5 animated slideInDown">How to Collect Them Properly</h4>
    </div>

    <!-- Tarjetas para instrucciones de recolección adecuada -->
    <div class="card mx-auto d-flex flex-row" style="width: 50rem; border: none">
        <div class="card-body me-5 bg-primary animated slideInDown shadow" style="min-height: 300px;">
            <h5 class="card-title text-white">Organic Waste</h5>
            <ul>
                <li class="text-white">Use biodegradable or compostable bags to store this waste.</li>
            </ul>
            <ul>
                <li class="text-white">Keep a separate container for organic waste in your kitchen or garden.</li>
            </ul>
            <ul>
                <li class="text-white">If you have access to a composter, place organic waste there to decompose naturally and create compost for plants.</li>
            </ul>
            <ul>
                <li class="text-white">Avoid mixing non-compostable materials like plastics in this container.</li>
            </ul>
        </div>

        <div class="card-body me-5 bg-primary animated slideInDown shadow" style="min-height: 300px;">
            <h5 class="card-title text-white">Recyclable Waste</h5>
            <ul>
                <li class="text-white">Paper and Cardboard: Ensure they are clean and dry. Fold cardboard to save space.</li>
            </ul>
            <ul>
                <li class="text-white">Glass: Rinse bottles and glass jars. Remove caps or labels if possible.</li>
            </ul>
            <ul>
                <li class="text-white">Plastics: Identify the type of plastic (check recycling codes) and rinse them before disposal.</li>
            </ul>
            <ul>
                <li class="text-white">Metals: Clean aluminum and metal cans, and compress them to save space.</li>
            </ul>
        </div>
    </div>

    <!-- Tarjetas para residuos no reciclables y peligrosos -->
    <div class="card mx-auto mt-5 d-flex flex-row" style="width: 50rem; border: none">
        <div class="card-body me-5 bg-primary animated slideInDown shadow" style="min-height: 300px;">
            <h5 class="card-title text-white">Non-Recyclable Waste</h5>
            <ul>
                <li class="text-white">Place this waste in sealed garbage bags to avoid spills and odors.</li>
            </ul>
            <ul>
                <li class="text-white">Use a designated and clearly labeled container for these wastes, separate from recyclables.</li>
            </ul>
            <ul>
                <li class="text-white">Check if your area has special programs for items like plastic bags, which may be recyclable at specific centers.</li>
            </ul>
        </div>

        <div class="card-body me-5 bg-primary animated slideInDown shadow" style="min-height: 300px;">
            <h5 class="card-title text-white">Hazardous Waste</h5>
            <ul>
                <li class="text-white">Batteries: Store used batteries in a safe container until you can take them to a specialized collection point.</li>
            </ul>
            <ul>
                <li class="text-white">Electronics: Don’t dispose of them in regular trash. Look for electronic waste collection points in your community.</li>
            </ul>
            <ul>
                <li class="text-white">Paints and oils: Keep them in their original containers and take them to hazardous waste management centers.</li>
            </ul>
            <ul>
                <li class="text-white">Chemical products: Never pour them down the drain. Use airtight containers and follow local disposal instructions.</li>
            </ul>
        </div>
